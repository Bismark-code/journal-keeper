"""
Модуль редактирования текста через локальную LLM (Ollama).
Принимает строку, возвращает отредактированную строку
с сохранением смысла и стиля.
"""

import sys
from typing import Optional

from .config import get_ollama_model

# Фиксированный системный промпт для редактора
EDITOR_SYSTEM_PROMPT = """Ты — аккуратный редактор личного дневника. Твоя задача — улучшить текст записи, строго соблюдая следующие правила:

РАЗРЕШАЕТСЯ:
- Разбивать слишком длинные предложения на более короткие, сохраняя смысл.
- Менять порядок слов для ясности и читаемости.
- Заменять неграмотные или корявые обороты на гладкие, литературные — но без канцелярита и без высокопарности.
- Добавлять недостающие знаки препинания и логические связки.
- Исправлять опечатки и орфографические ошибки.

ЗАПРЕЩАЕТСЯ:
- Выбрасывать детали, примеры, уточнения — даже если они кажутся избыточными.
- Упрощать сложные мысли; автор имеет право на сложность.
- Менять оценку, эмоциональную окраску или точку зрения автора.
- Добавлять отсебятину: комментарии, пояснения, суждения от своего лица.
- Менять стиль автора на «правильный» — сохраняй индивидуальность и интонацию.
- Удалять повторы, если они несут смысл (эмоциональный, стилистический).

Выведи ТОЛЬКО отредактированный текст, без пояснений, комментариев или обёрток."""


def edit_text(original_text: str, model: Optional[str] = None) -> str:
    """
    Отправляет текст в Ollama для редактирования и возвращает результат.

    Если Ollama не запущена или модель недоступна —
    возвращает исходный текст с предупреждением.

    Параметры:
        original_text: исходный текст для редактирования
        model: имя модели Ollama (если None — берётся из конфигурации)

    Возвращает:
        Отредактированный текст или исходный при ошибке.
    """
    model_name = model or get_ollama_model()

    try:
        import ollama

        response = ollama.chat(
            model=model_name,
            messages=[
                {"role": "system", "content": EDITOR_SYSTEM_PROMPT},
                {"role": "user", "content": original_text},
            ],
        )
        edited = response.get("message", {}).get("content", "")
        if edited.strip():
            return edited.strip()
        else:
            print("[ПРЕДУПРЕЖДЕНИЕ] LLM вернула пустой ответ. Возвращён исходный текст.")
            return original_text

    except ImportError:
        print(
            "[ПРЕДУПРЕЖДЕНИЕ] Библиотека 'ollama' не установлена. "
            "Установите: pip install ollama",
            file=sys.stderr,
        )
        return original_text

    except Exception as e:
        print(
            f"[ПРЕДУПРЕЖДЕНИЕ] Ollama недоступна ({type(e).__name__}: {e}). "
            "Возвращён исходный текст.",
            file=sys.stderr,
        )
        return original_text

